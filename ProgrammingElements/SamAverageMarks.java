class SamAverageMarks {
    public static void main(String[] args) {
        double maths = 94;
        double physics = 95;
        double chemistry = 96;

        double obtainedMarks = maths + physics + chemistry;
        double avg = obtainedMarks / 3;

        System.out.println("Sam's average marks in PCM is " + avg);
    }
}